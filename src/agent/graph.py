"""
LangGraph workflow definition for LinkedIn candidate sourcing.

This module defines the graph structure, state, and execution flow for an
agentic pipeline that converts user hiring requirements into LinkedIn
candidate search results.

Pipeline Overview:
    1. Accept user input (`user_prompt`)
    2. Generate a LinkedIn Boolean search query using an LLM
    3. Perform a Google X-ray search via Serper API
    4. Extract and return LinkedIn profile results

Core Components:
    - State:
        Shared data object passed between nodes (input → query → results)

    - Context:
        Runtime configuration for controlling execution behavior

    - Nodes:
        - build_search_query_linkedin: Generates Boolean query from user input
        - search_linkedin_profiles: Fetches LinkedIn profiles using Serper API

    - Graph:
        Defines the execution order and data flow between nodes

Usage:
    - Initialize the graph
    - Invoke with input state:
        graph.invoke({"user_prompt": "<hiring requirement>"})

    - Optional runtime config:
        graph.invoke(input_state, config={"configurable": {...}})

Notes:
    - Designed for extensibility (can add more nodes like ranking, enrichment)
    - Uses external APIs (LLM + Serper) for query generation and search
    - Does not perform data enrichment or validation by default

Dependencies:
    - LangGraph
    - requests
    - LLM API (configured via environment variables)
    - Serper API for search

Environment Variables:
    - LLM_BASE_URL
    - SERPER_BASE_URL
    - SERPER_API_KEY
    - XRAY_PREFIX

"""

from __future__ import annotations
from typing import Any, Dict, List
import json
from langgraph.graph import StateGraph, START, END
from langgraph.runtime import Runtime
from typing_extensions import TypedDict

