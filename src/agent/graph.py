import os

import requests

from prompt_templates.linkedin_query_prompt import LINKEDIN_QUERY_PROMPT
from prompt_templates.user_intent_prompt import INTENT_CLASSIFIER_PROMPT

class Context(TypedDict):
    """
    Runtime configuration context for the LangGraph agent.

    This TypedDict defines configurable parameters that can be passed at
    graph initialization or invocation time. Unlike `State`, which carries
    data through the workflow, `Context` is used to control execution behavior,
    feature flags, and environment-specific settings.

    Fields:
        my_configurable_param (str):
            Example configurable parameter.
            Can be used to modify node behavior dynamically (e.g., toggle features,
            switch environments, adjust limits, or inject runtime configs).

    Usage:
        - Passed during graph invocation:
            graph.invoke(input_state, config={"configurable": {...}})

        - Accessed inside nodes via:
            runtime.context.get("my_configurable_param")

    Notes:
        - Context is read-only during execution
        - Ideal for passing non-user, system-level configurations
        - Helps separate business logic (State) from runtime behavior (Context)

    Reference:
        LangGraph Context Docs:
        https://langchain-ai.github.io/langgraph/cloud/how-tos/configuration_cloud/
    """
    
class State(TypedDict):
    """
    Shared state object passed between LangGraph nodes.

    This TypedDict defines the structure of data flowing through the agent pipeline.
    Each node reads from and writes to this state, enabling step-by-step data
    transformation (e.g., prompt → query → search results).

    Fields:
        user_prompt (str):
            Raw user input describing hiring requirements.
            Example: "Looking for JavaScript developers with React in India"

        linkedin_query (str):
            Boolean search query generated by the LLM for LinkedIn sourcing.
            Example: ('("Frontend Developer" OR "Javascript Developer") AND ("React") AND ("India")')

        linkedin_results (List[Dict]):
            List of LinkedIn profile search results fetched via Serper API.
            Each item typically contains:
                - title (str): Search result title
                - snippet (str): Short description
                - url (str): LinkedIn profile link

    Notes:
        - This state is mutable and shared across all LangGraph nodes
        - Each node is responsible for updating only relevant fields
        - Missing keys may cause runtime errors if not handled properly
        - Consider using `.get()` when accessing optional fields

    Reference:
        LangGraph State Docs:
        https://langchain-ai.github.io/langgraph/concepts/low_level/#state
    """
    user_prompt: str
    user_prompt_intent: str
    query_response: str
    linkedin_results: List[str]

def build_search_query_linkedin(state: State, runtime: Runtime[Context]) -> Dict[str, Any]:
    """
    Generate a LinkedIn Boolean search query from user input using an LLM.

    This function acts as a LangGraph node that transforms a natural language
    hiring requirement (`user_prompt`) into a structured LinkedIn search query.
    The generated query is stored back into the shared state under `linkedin_query`.

    Workflow:
        1. Extract `user_prompt` from the state
        2. Format the prompt using `LINKEDIN_QUERY_PROMPT`
        3. Send request to LLM API (streaming enabled)
        4. Aggregate streamed response chunks
        5. Store final query in state

    Args:
        state (State):
            Shared mutable state across LangGraph nodes.
            Expected to contain:
                - user_prompt (str): Raw hiring requirement input

        runtime (Runtime[Context]):
            LangGraph runtime context (currently unused).
            Can be used for logging, configuration, or metadata injection.

    Returns:
        Dict[str, Any]:
            Updated state containing:
                - linkedin_query (str): Generated LinkedIn Boolean search query

    Raises:
        requests.exceptions.RequestException:
            If the LLM API call fails

        json.JSONDecodeError:
            If streaming response contains invalid JSON

    Environment Variables:
        LLM_BASE_URL (str):
            Base URL of the LLM API (e.g., http://localhost:11434)

    Notes:
        - Uses streaming response (`iter_lines`) to handle token-wise generation
        - Assumes LLM returns line-delimited JSON with a `response` field
        - No validation is applied to the generated query (should be handled downstream)
    """  
    LLM_BASE_URL = os.getenv("LLM_BASE_URL")
    
    prompt = LINKEDIN_QUERY_PROMPT.format(
        user_input=state.get('user_prompt')
    )
    
    data = {
    "model": "qwen2.5",
    "prompt": prompt
    }
        
    response = requests.post(
        LLM_BASE_URL+"/api/generate", 
        json=data, 
        stream=True
    )
        
    full_response = ""
    
    for line in response.iter_lines():
        if line:
            chunk = json.loads(line)
            full_response += chunk.get("response", "")
            print("full_response INSIDE::::::::::::::::::::::;", full_response)
    
    print("FULL RESPONSE OUTSIDE LOOP IS is:::::::::::::::::::::::::", full_response)
    
    state['query_response'] = full_response
    
    return state

def search_linkedin_profiles(state: State, runtime: Runtime[Context]) -> Dict[str, Any]:
    """
    Fetch LinkedIn profile search results using Serper (Google Search API).

    This function acts as a LangGraph node that performs a Google X-ray search
    to retrieve LinkedIn profile URLs based on a previously generated Boolean query.
    It filters and stores relevant LinkedIn profile results into the shared state.

    Workflow:
        1. Read `linkedin_query` from state
        2. Prefix query with X-ray filter (e.g., site:linkedin.com/in)
        3. Send search request to Serper API
        4. Parse organic search results
        5. Filter only LinkedIn profile URLs
        6. Store structured results in state

    Args:
        state (State):
            Shared mutable state across LangGraph nodes.
            Expected to contain:
                - linkedin_query (str): Boolean search query generated by LLM

        runtime (Runtime[Context]):
            LangGraph runtime context (currently unused).
            Can be used for logging, tracing, or configuration injection.

    Returns:
        Dict[str, Any]:
            Updated state containing:
                - linkedin_results (List[Dict]): List of extracted LinkedIn profiles
                    Each item includes:
                        - title (str): Page title from search result
                        - snippet (str): Short description from search result
                        - url (str): LinkedIn profile URL

    Raises:
        requests.exceptions.RequestException:
            If the Serper API request fails

        ValueError:
            If response JSON is invalid or missing expected fields

    Environment Variables:
        SERPER_BASE_URL (str):
            Base URL for Serper API (e.g., https://google.serper.dev)

        SERPER_API_KEY (str):
            API key for authenticating Serper requests

        XRAY_PREFIX (str):
            Google X-ray search prefix (e.g., "site:linkedin.com/in")

    Notes:
        - Only results containing "linkedin.com/in" are included
        - Limits results to top 10 (configurable via payload)
        - Does not perform deduplication across multiple queries
        - Output is lightweight (no enrichment or structured parsing)
    """
    base_url =  os.getenv("SERPER_BASE_URL")
    
    endpoint = base_url+"/search"
    
    searchQuery = f'site:linkedin.com/in {state.get("query_response")}'
    
    print("searchQuery::::::::::::", searchQuery)
    
    payload = {
        "q": searchQuery,
        "num": 10
    }

    headers = {
        "X-API-KEY": os.getenv("SERPER_API_KEY"),
        "Content-Type": "application/json"
    }

    response = requests.post(endpoint, json=payload, headers=headers)
    data = response.json()
    
    results = []

    for item in data.get("organic", []):
        link = item.get("link", "")
        
        if "linkedin.com/in" in link:
            results.append({
                "title": item.get("title"),
                "snippet": item.get("snippet"),
                "url": link
            })
    if(len(results)):
        state['query_response'] = results
        
    return state

